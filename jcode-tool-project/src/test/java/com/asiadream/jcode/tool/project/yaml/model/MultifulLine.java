package com.asiadream.jcode.tool.project.yaml.model;

public class MultifulLine {
    //
    private String includeNewlines;
    private String foldNewlines;
    private String windowsDrive;
    private String containExpression;

    public String getIncludeNewlines() {
        return includeNewlines;
    }

    public void setIncludeNewlines(String includeNewlines) {
        this.includeNewlines = includeNewlines;
    }

    public String getFoldNewlines() {
        return foldNewlines;
    }

    public void setFoldNewlines(String foldNewlines) {
        this.foldNewlines = foldNewlines;
    }

    public String getWindowsDrive() {
        return windowsDrive;
    }

    public void setWindowsDrive(String windowsDrive) {
        this.windowsDrive = windowsDrive;
    }

    public String getContainExpression() {
        return containExpression;
    }

    public void setContainExpression(String containExpression) {
        this.containExpression = containExpression;
    }
}
